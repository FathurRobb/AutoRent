    private void cmdTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdTambahActionPerformed
        // TODO add your handling code here:
        if(jfc.showOpenDialog(LabelFoto)==JFileChooser.APPROVE_OPTION){

            Toolkit toolkit=Toolkit.getDefaultToolkit();
            Image image=toolkit.getImage(jfc.getSelectedFile().getAbsolutePath());
            Image imagedResized=image.getScaledInstance(LabelFoto.getWidth(),LabelFoto.getHeight(), Image.SCALE_DEFAULT);
            ImageIcon imageIcon=new ImageIcon(imagedResized);

            LabelFoto.setIcon(imageIcon);
            TxtFoto.setText(jfc.getSelectedFile().getName());

        }
    }//GEN-LAST:event_cmdTambahActionPerformed

    private void cmdTambah1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdTambah1ActionPerformed
        // TODO add your handling code here:
        String Jenkel="";
        if (RbLaki.isSelected()) {
            Jenkel = RbLaki.getText();
        }else{
            Jenkel = RbPerempuan.getText();
        }

        try{
            File file = new File(jfc.getSelectedFile().getPath()); // file untuk dikopi
            FileInputStream fis=new FileInputStream(file);
            java.sql.Connection con=(Connection)koneksi;
            PreparedStatement ps=con.prepareStatement("update karyawan set nik_kar=? nama_kar=?, alamat_kar=?, jenkel_kar=?, agama_kar=?, no_hp_kar=?, no_sim_kar=?, foto_kar=?, id_status_sopir=?, id_jabatan=? where nik_kar=?");
            
            ps.setString(1,TxtNIK.getText());
            ps.setString(2,TxtNama.getText());
            ps.setString(3,TAAlamat.getText());
            ps.setString(4,Jenkel);
            ps.setObject(5,CBBAgama.getSelectedItem());
            ps.setString(6,TxtHp.getText());
            ps.setString(7,TxtSIM.getText());
            ps.setBinaryStream(8,fis,(int)file.length());
            ps.setString(9,"2");
            ps.setString(10,TxtIdJabatan.getText());
            ps.executeUpdate();

            ps.close();
            fis.close();
            con.close();

            JOptionPane.showMessageDialog(null, "Penyimpanan Data Berhasil");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        SAKaryawan sakart = new SAKaryawan();
        sakart.setVisible(true);
        dispose();
    }//GEN-LAST:event_cmdTambah1ActionPerformed

