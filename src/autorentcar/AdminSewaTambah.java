    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
        if (jComboBox1.getSelectedItem() == "Berkendara Sendiri") {
            TxtHargaSewa.setText("0");
            jLabel11.setVisible(true);
            TxtSIM.setVisible(true);
            jLabel13.setVisible(false);
            jComboBox2.setVisible(false);
        }else if (jComboBox1.getSelectedItem() == "Berkendara Dengan Sopir") {
            TxtHargaSewa.setText("100000");
            jLabel13.setVisible(true);
            jComboBox2.setVisible(true);
            jLabel11.setVisible(false);
            TxtSIM.setVisible(false);
        }
        try {
            DateFormat format = new SimpleDateFormat("dd MMMM yyyy");
            Date tanggalmulai = format.parse(mulai);
            Date tanggalakhir = format.parse(akhir);
            long tanggalmulai1 = tanggalmulai.getTime();
            long tanggalakhir1 = tanggalakhir.getTime();
            long diff = tanggalakhir1 - tanggalmulai1;
            long lama = diff / (24 * 60 * 60 * 1000);
            TxtSelisih.setText(Long.toString(lama) + "");
        } catch (Exception e) {
            System.out.println("" + e.getMessage());
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

