    public void profil(){
        try{
            Statement stmt = koneksi.createStatement();
            String tampil = "SELECT*FROM karyawan WHERE nik_kar='"+nik_kar1+"'";
            ResultSet rs = stmt.executeQuery(tampil);
            while(rs.next()){
                String nama_kar1 = rs.getString("nama_kar");
                jLabel22.setText(nama_kar1);
            }
        }catch (SQLException ex){
            ex.printStackTrace();
        }
    }
    
    public void comboNamaKar(){
        try{
            String sql = "SELECT nama_kar FROM karyawan WHERE id_jabatan = 3 AND id_status_sopir = 2";
            Statement stmt = koneksi.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            while (rs.next()) {
                
                jComboBox2.addItem(rs.getString("nama_kar"));
            }
            
            rs.close();
            stmt.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        } 
    }
    
    public void ambilNikKar(){
        try{
            String sql = "SELECT nik_kar FROM karyawan WHERE nama_kar = '"+jComboBox2.getSelectedItem()+"'";
            Statement stmt = koneksi.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            while (rs.next()) {
                
                TxtNikKar.setText(rs.getString("nik_kar"));
            }
            
            rs.close();
            stmt.close();
        }catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
    public void idPenyewa(){
        try{
            Statement stmt = koneksi.createStatement();
            String sql = "SELECT max(id_penyewa) as idSewa FROM sewa";
            ResultSet rs = stmt.executeQuery(sql);
            if (rs.next()) {
                String id = rs.getString("idSewa");
                TxtIDPenyewa.setText(id);
            }
        }catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
