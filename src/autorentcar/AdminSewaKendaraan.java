    private void tbl_kendaraanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_kendaraanMouseClicked
        // TODO add your handling code here:
        try {
            int baris = tbl_kendaraan.rowAtPoint(evt.getPoint());
            TableModel model = tbl_kendaraan.getModel();
            String no_polisi =tbl_kendaraan.getValueAt(baris, 1).toString();
            String harga_sewa_mobil =tbl_kendaraan.getValueAt(baris, 7).toString();
            ast.TxtMobil.setText(no_polisi);
            ast.TxtHargaMobil.setText(harga_sewa_mobil);
            Statement stmt = koneksi.createStatement();
            String tampil = "SELECT*FROM kendaraan WHERE no_polisi='"+no_polisi+"'";
            ResultSet rs = stmt.executeQuery(tampil);
            while(rs.next()){
                Blob gambar = (Blob) rs.getBlob("gambar_mobil");
                int ukuran = (int) (gambar.length());
                ImageIcon tampilkan = new ImageIcon(new ImageIcon(gambar.getBytes(1, ukuran))
                        .getImage().getScaledInstance(240, 180, Image.SCALE_DEFAULT));
                jButton1.setIcon(tampilkan);
            }
        } catch (SQLException ex) {
            Logger.getLogger(AdminSewaKendaraan.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_tbl_kendaraanMouseClicked

