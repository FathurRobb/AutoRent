    public void showData(){
        String[] kolom = {"NO","Nomor Polisi","Jenis Mobil","Merk","Tipe Mobil","Tahun","Kapasitas","Harga Sewa"};
        
        dtm = new DefaultTableModel(null, kolom);
        try{
            Statement stmt = koneksi.createStatement();
            String query = "SELECT no_polisi, jenis_mobil, merk_mobil, tipe_mobil, tahun_mobil, kapasitas_mobil, harga_sewa_mobil FROM kendaraan WHERE id_status = 2";
            ResultSet rs = stmt.executeQuery(query);
            int no = 1;
            while (rs.next()){
                String no_polisi = rs.getString("no_polisi");
                String jenis_mobil = rs.getString("jenis_mobil");
                String merk_mobil = rs.getString("merk_mobil");
                String tipe_mobil = rs.getString("tipe_mobil");
                String tahun_mobil = rs.getString("tahun_mobil");
                String kapasitas_mobil = rs.getString("kapasitas_mobil");
                String harga_sewa_mobil = rs.getString("harga_sewa_mobil");
                
                dtm.addRow(new String[]{no+"",no_polisi,jenis_mobil,tipe_mobil,merk_mobil,tahun_mobil,kapasitas_mobil,harga_sewa_mobil});
                no++;
            }
        }catch (SQLException ex){
            ex.printStackTrace();
        }
        tbl_kendaraan.setModel(dtm);
    }
    
