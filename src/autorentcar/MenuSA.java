    private void BtnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCariActionPerformed
        // TODO add your handling code here:
        String cari = TxtCari.getText();
        int clean = dtm.getRowCount();
        for (int i = 0; i < clean; i++) {
            dtm.removeRow(0);
        }
        try{
            Statement stmt = koneksi.createStatement();
            String sql = ("SELECT * FROM sewa WHERE nama_penyewa LIKE '"+cari+"%' ;");
            ResultSet result = stmt.executeQuery(sql);
            int no = 1;
            while (result.next()){
                String nama = result.getString("nama_penyewa");
                String tgl_mulai = result.getString("tgl_mulai_sewa");
                String tgl_akhir = result.getString("tgl_akhir_sewa");
                String status = result.getString("status_sewa");
                String no_polisi = result.getString("no_polisi");

                dtm.addRow(new String[]{no+"",nama,tgl_mulai,tgl_akhir,status,no_polisi});
                no++;
            }
            tbl_sewa.setModel(dtm);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_BtnCariActionPerformed

