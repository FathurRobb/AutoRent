            java.sql.PreparedStatement pst=conn.prepareStatement(sql2);
            pst.execute();
            String sql3 = "UPDATE sewa, karyawan SET karyawan.id_status_sopir = 1 WHERE sewa.nik_kar = karyawan.nik_kar AND sewa.tgl_mulai_sewa = CAST(CURRENT_TIMESTAMP AS DATE)";
            java.sql.Connection conn3=(Connection)koneksi;
