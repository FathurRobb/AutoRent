    public void showData(){
        String[] kolom = {"NO","Nama Karyawan","Jenis Kelamin","Agama","Jabatan"};
        
        dtm = new DefaultTableModel(null, kolom);
        try{
            Statement stmt = koneksi.createStatement();
            String query = "SELECT k.nama_kar, k.jenkel_kar, k.agama_kar, j.nama_jabatan FROM karyawan AS k INNER JOIN jabatan AS j ON k.id_jabatan = j.id_jabatan  ORDER BY j.nama_jabatan ASC";
            ResultSet rs = stmt.executeQuery(query);
            int no = 1;
            while (rs.next()){
                String nama_kar = rs.getString("nama_kar");
                String jenkel_kar = rs.getString("jenkel_kar");
                String agama_kar = rs.getString("agama_kar");
                String nama_jabatan = rs.getString("nama_jabatan");
                dtm.addRow(new String[]{no+"",nama_kar,jenkel_kar,agama_kar,nama_jabatan});
                no++;
            }
        }catch (SQLException ex){
            ex.printStackTrace();
        }
        tbl_karyawan.setModel(dtm);
    }
    
