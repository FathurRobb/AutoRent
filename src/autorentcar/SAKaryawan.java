    private void BtnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCariActionPerformed
        // TODO add your handling code here:
        String cari = TxtCari.getText();
        int clean = dtm.getRowCount();
        for (int i = 0; i < clean; i++) {
            dtm.removeRow(0);
        }
        try{
            Statement stmt = koneksi.createStatement();
            String sql = ("SELECT k.nama_kar, k.jenkel_kar, k.agama_kar, j.nama_jabatan FROM karyawan AS k INNER JOIN jabatan AS j ON k.id_jabatan = j.id_jabatan WHERE nama_kar LIKE '%"+cari+"%' OR agama_kar LIKE '%"+cari+"%' OR nama_jabatan LIKE '%"+cari+"%'; ");
            ResultSet rs = stmt.executeQuery(sql);
            int no = 1;
            while (rs.next()){
                String nama_kar = rs.getString("nama_kar");
                String jenkel_kar = rs.getString("jenkel_kar");
                String agama_kar = rs.getString("agama_kar");
                String nama_jabatan = rs.getString("nama_jabatan");
                dtm.addRow(new String[]{no+"",nama_kar,jenkel_kar,agama_kar,nama_jabatan});
                no++;
            }
            tbl_karyawan.setModel(dtm);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_BtnCariActionPerformed

