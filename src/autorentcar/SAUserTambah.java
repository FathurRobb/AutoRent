    private void cmdTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdTambahActionPerformed
        // TODO add your handling code here:
        try{
            String sql = "SELECT username FROM user";
            java.sql.Connection conn=(Connection)koneksi;
            java.sql.PreparedStatement pst=conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            String cekUsername = TxtUsername.getText();
            if (rs.next()) {
                String username = rs.getString("username");
                if (username.equals(cekUsername)) {
                    JOptionPane.showMessageDialog(null, "Username sudah ada");
                }else{
                    String sql2 = "INSERT INTO user VALUES('"+cekUsername+"','"+TxtPassword.getText()+"','"+TxtIdHak.getText()+"','"+TxtNikKar.getText()+"')";
                    Statement stmt = koneksi.createStatement();
                    stmt.execute(sql2);
                    JOptionPane.showMessageDialog(null, "Username berhasil ditambahkan");
                    SAUser sau = new SAUser();
                    sau.setVisible(true);
                    dispose();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_cmdTambahActionPerformed

