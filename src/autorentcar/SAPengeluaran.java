    public void showData(){
        String[] kolom = {"NO","Plat Nomor","Keterangan","Tanggal Pengeluaran","Total Pengeluaran"};
        
        dtm = new DefaultTableModel(null, kolom);
        try{
            Statement stmt = koneksi.createStatement();
            String query = "SELECT no_polisi, keterangan_pengeluaran, tgl_pengeluaran, total_pengeluaran FROM  pengeluaran ";
            ResultSet rs = stmt.executeQuery(query);
            int no = 1;
            while (rs.next()){
                String no_polisi = rs.getString("no_polisi");
                String keterangan_pengeluaran = rs.getString("keterangan_pengeluaran");
                String tgl_pengeluaran = rs.getString("tgl_pengeluaran");
                String total_pengeluaran = rs.getString("total_pengeluaran");
                
                dtm.addRow(new String[]{no+"",no_polisi,keterangan_pengeluaran,tgl_pengeluaran,total_pengeluaran});
                no++;
            }
        }catch (SQLException ex){
            ex.printStackTrace();
        }
        tbl_pengeluaran.setModel(dtm);
    }
    
