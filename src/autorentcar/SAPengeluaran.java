    private void BtnPrint1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnPrint1ActionPerformed
        // TODO add your handling code here:
        int pemasukan = 0;
        String bulan = "";
        try{
            Statement stmt = koneksi.createStatement();
            String query = "SELECT SUM(total_bayar_sewa) AS 'Total Pemasukan', MONTHNAME(tgl_mulai_sewa) AS 'Bulan' FROM sewa WHERE YEAR(tgl_mulai_sewa) = YEAR(CURRENT_DATE) GROUP BY MONTH(tgl_mulai_sewa), YEAR(tgl_mulai_sewa)";
            java.sql.Connection conn=(Connection)koneksi;
            java.sql.PreparedStatement pst=conn.prepareStatement(query);
            ResultSet rs = pst.executeQuery();
            DefaultCategoryDataset dcd = new DefaultCategoryDataset();
            while (rs.next()){
                pemasukan = rs.getInt("Total Pemasukan");
                bulan = rs.getString("Bulan");
                dcd.setValue(pemasukan, bulan, bulan);
            }
            JFreeChart chart = ChartFactory.createBarChart("Pemasukan Tahun ini", "Bulan", "Pemasukan", dcd, PlotOrientation.VERTICAL, false, true, false);
            CategoryPlot p = chart.getCategoryPlot();
            ChartFrame frame = new ChartFrame("Data Keuangan Tahun ini",chart);
            frame.setVisible(true);
            frame.setSize(400,650);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_BtnPrint1ActionPerformed

