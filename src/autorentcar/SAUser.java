    public void showData(){
        String[] kolom = {"NO","Nama Karyawan","Username","Jabatan","Hak Akses"};
        
        dtm = new DefaultTableModel(null, kolom);
        try{
            Statement stmt = koneksi.createStatement();
            String query = "SELECT k.nama_kar, u.username, j.nama_jabatan, ha.nama_hak_akses FROM user AS u INNER JOIN hak_akses AS ha ON u.id_hak_akses = ha.id_hak_akses INNER JOIN karyawan AS k ON u.nik_kar = k.nik_kar INNER JOIN jabatan AS j ON k.id_jabatan = j.id_jabatan ";
            ResultSet rs = stmt.executeQuery(query);
            int no = 1;
            while (rs.next()){
                String nama_kar = rs.getString("nama_kar");
                String username = rs.getString("username");
                String nama_jabatan = rs.getString("nama_jabatan");
                String nama_hak_akses = rs.getString("nama_hak_akses");
                
                dtm.addRow(new String[]{no+"",nama_kar,username,nama_jabatan,nama_hak_akses});
                no++;
            }
        }catch (SQLException ex){
            ex.printStackTrace();
        }
        tbl_user.setModel(dtm);
    }
